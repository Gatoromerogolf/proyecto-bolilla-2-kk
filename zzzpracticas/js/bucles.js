//for
for (let i = 0; i < 10; i++) {
  console.log("numero: " + i);
}

// while
let j = 0;
while (j < 5) {
  console.log(j + 1);
  j++;
}

// do - while
let h = 10;
do {
  console.log(h);
  h++;
} while (h <= 15);

//  for of
let score = [13, 20, 38, 10];
for (let valor of score) {
  console.log(valor);
}
